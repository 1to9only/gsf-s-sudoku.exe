char* maxcanon(Canon_t* canon, Grid_t* g, char* puz, char* can, int empty, int transpose)
{
   char puzzle[ 82];
   for (int i=0; i<81; i++ )
   {
      if ( SOLVED( g, i) )
      {
         puzzle[ i] = '0' + data.ident[ g->cell[i]];
      }
      else
      {
         puzzle[ i] = '.';
      }
   }
   puzzle[ 81] = 0;
   strcpy( can, maxlex( puzzle));
   return can;
}

